/*****************************************************************************
 结构名    :OM_BST_HEADER_STRU
 结构说明  :
*****************************************************************************/
typedef struct
{
    VOS_UINT8                           ucFuncType;
    VOS_UINT8                           ucReserved;
    VOS_UINT16                          usLength;
    VOS_UINT32                          ulSn;
    VOS_UINT32                          ulTimeStamp;
}OM_BST_HEADER_STRU;

/*****************************************************************************
 结构名    :OM_BST_MSG_STRU
 结构说明  : 
*****************************************************************************/
typedef struct
{
    OM_BST_HEADER_STRU                  stBstOmHead;
    VOS_UINT16                          usPrimId;
    VOS_UINT16                          usToolId;
    VOS_UINT8                           aucData[4];
}OM_BST_MSG_STRU;

/*****************************************************************************
 结构名    :OM_BST_APP_INFO_STRU
 结构说明  : 
*****************************************************************************/
typedef struct
{
    OM_BST_APP_TYPE_ENUM_UINT16         usAppType;      /*代理应用类型*/
    VOS_UINT16                          usTaskId;       /*任务ID*/
    VOS_UINT16                          usTaskState;    /*任务运行状态*/
    VOS_UINT16                          usCycleLen;     /*循环长度*/
    VOS_UINT16                          usSuccNum;      /*成功次数*/
    VOS_UINT16                          usErrorNum;     /*失败次数*/
    VOS_UINT16                          usRemotePort;   /*远程IP端口号*/
    VOS_UINT16                          usLocalPort;    /*本地IP端口号*/
    VOS_UINT8                           aucRemoteIp[OM_BST_IP_ADDR_LEN];/*远程IP地址*/
    VOS_UINT8                           aucLocalIp[OM_BST_IP_ADDR_LEN]; /*本地IP地址*/
    VOS_UINT32                          ulTxFlow;       /*发送流量统计*/
    VOS_UINT32                          ulRxFlow;       /*接收流量统计*/
}OM_BST_APP_INFO_STRU;


/*****************************************************************************
 结构名    :OM_BST_AGENT_APP_NUM_STRU
 结构说明  : 
*****************************************************************************/
typedef struct
{
    VOS_UINT16                          usReserved;
    VOS_UINT16                          usAgentAppNum;
    OM_BST_APP_INFO_STRU                stBstAppList[OM_BST_MNTN_APP_NUM];
}OM_BST_AGENT_APP_NUM_STRU;

