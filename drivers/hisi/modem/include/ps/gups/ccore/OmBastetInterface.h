/*****************************************************************************
 枚举名    : 定义上报Mntn消息标识，Bastet仅有一个上限报告标识
 协议表格  :
 ASN.1描述 :
 枚举说明  : ID_BST_OM_AGENT_INFO_IND, 上报Maintain消息
*****************************************************************************/
enum OM_BST_MSG_TYPE_ENUM
{
    ID_BST_OM_AGENT_INFO_IND    = 0xB06F,
    ID_BST_OM_MSG_TYPE_BUTT     = 0xFFFF,
};
typedef VOS_UINT16                      OM_BST_MSG_TYPE_ENUM_UINT16;

/*****************************************************************************
 枚举名    : 定义Bstet代理任务类型
 协议表格  :
 ASN.1描述 :
 枚举说明  :
    ID_BST_OM_TASK_TYPE_MAIN,       主任务
    ID_BST_OM_TASK_TYPE_EMAIL,      Email代理邮件查询任务
    ID_BST_OM_TASK_TYPE_HEARTBEAT,  心跳代理业务
*****************************************************************************/
enum OM_BST_TASK_TYPE_ENUM
{
    ID_BST_OM_TASK_TYPE_MIN             = 0,
    ID_BST_OM_TASK_TYPE_MAIN,
    ID_BST_OM_TASK_TYPE_EMAIL,
    ID_BST_OM_TASK_TYPE_HEARTBEAT,
    ID_BST_OM_TASK_TYPE_BUTT
};
typedef VOS_UINT16                      OM_BST_APP_TYPE_ENUM_UINT16;

